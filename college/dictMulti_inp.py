# Using Lists as Values
my_dict = {
    'apple': [4, 'red', 1.2],
    'banana': [3, 'yellow', 0.5],
    'orange': [2, 'orange', 0.8],
    'pear': [1, 'green', 1.5]
}

# Accessing elements
print("Apple details:", my_dict['apple'])
print("Banana quantity:", my_dict['banana'][0])
print("Orange color:", my_dict['orange'][1])
print("Pear price:", my_dict['pear'][2])
